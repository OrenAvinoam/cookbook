const LangContext = createContext({ lang: "en", setLang: () => {}, tr: k => k, isRTL: false });

export function LangProvider({ children }) {
  const [lang, setLangRaw] = useState(() => {
    try { return localStorage.getItem("app_lang") || "en"; } catch { return "en"; }
  });
  const setLang = useCallback((l) => {
    setLangRaw(l);
    try { localStorage.setItem("app_lang", l); } catch {}
  }, []);
  const tr = useCallback((key, ...args) => {
    const val = strings[lang]?.[key] ?? strings.en[key];
    if (typeof val === "function") return val(...args);
    return val ?? key;
  }, [lang]);
  const isRTL = lang === "he";
  return (
    <LangContext.Provider value={{ lang, setLang, tr, isRTL }}>
      {children}
    </LangContext.Provider>
  );
}

export function useLanguage() { return useContext(LangContext); }
